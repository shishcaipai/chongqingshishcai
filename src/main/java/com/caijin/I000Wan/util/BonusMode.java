package com.caijin.I000Wan.util;

public class BonusMode {
  private  String  unit;
  private  String  decade;
  private  String  hundredsplace;
  private  String  thousand;
  private  String  myriabit;
public String getUnit() {
	return unit;
}
public void setUnit(String unit) {
	this.unit = unit;
}
public String getDecade() {
	return decade;
}
public void setDecade(String decade) {
	this.decade = decade;
}
public String getHundredsplace() {
	return hundredsplace;
}
public void setHundredsplace(String hundredsplace) {
	this.hundredsplace = hundredsplace;
}
public String getThousand() {
	return thousand;
}
public void setThousand(String thousand) {
	this.thousand = thousand;
}
public String getMyriabit() {
	return myriabit;
}
public void setMyriabit(String myriabit) {
	this.myriabit = myriabit;
}
  
}
